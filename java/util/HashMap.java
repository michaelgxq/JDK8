    /**
     * Returns the value to which the specified key is mapped,
     * or {@code null} if this map contains no mapping for the key.
     *
     * <p>More formally, if this map contains a mapping from a key
     * {@code k} to a value {@code v} such that {@code (key==null ? k==null :
     * key.equals(k))}, then this method returns {@code v}; otherwise
     * it returns {@code null}.  (There can be at most one such mapping.)
     *
     * <p>A return value of {@code null} does not <i>necessarily</i>
     * indicate that the map contains no mapping for the key; it's also
     * possible that the map explicitly maps the key to {@code null}.
     * The {@link #containsKey containsKey} operation may be used to
     * distinguish these two cases.
     *
     * @see #put(Object, Object)
     */
    public V get(Object key) {
        Node<K,V> e;
        return (e = getNode(hash(key), key)) == null ? null : e.value;
    }

    /**
     * Implements Map.get and related methods
     *
     * @param hash hash for key
     * @param key the key
     * @return the node, or null if none
     */
    final Node<K,V> getNode(int hash, Object key) {
        Node<K,V>[] tab; Node<K,V> first, e; int n; K k;
        if ((tab = table) != null && (n = tab.length) > 0 &&
            (first = tab[(n - 1) & hash]) != null) {
            if (first.hash == hash && // always check first node
                ((k = first.key) == key || (key != null && key.equals(k))))
                return first;
            if ((e = first.next) != null) {
                if (first instanceof TreeNode)
                    return ((TreeNode<K,V>)first).getTreeNode(hash, key);
                do {
                    if (e.hash == hash &&
                        ((k = e.key) == key || (key != null && key.equals(k))))
                        return e;
                } while ((e = e.next) != null);
            }
        }
        return null;
    }

    /**
     * Returns <tt>true</tt> if this map contains a mapping for the
     * specified key.
     *
     * @param   key   The key whose presence in this map is to be tested
     * @return <tt>true</tt> if this map contains a mapping for the specified
     * key.
     */
    public boolean containsKey(Object key) {
        return getNode(hash(key), key) != null;
    }

    /**
     * Associates the specified value with the specified key in this map.
     * If the map previously contained a mapping for the key, the old
     * value is replaced.
     *
     * @param key key with which the specified value is to be associated
     * @param value value to be associated with the specified key
     * @return the previous value associated with <tt>key</tt>, or
     *         <tt>null</tt> if there was no mapping for <tt>key</tt>.
     *         (A <tt>null</tt> return can also indicate that the map
     *         previously associated <tt>null</tt> with <tt>key</tt>.)
     */
    public V put(K key, V value) {
        // 调用 putVal() 方法
        // 这里首先会调用 hash() 方法对键（Key）进行哈希运算
        return putVal(hash(key), key, value, false, true);
    }

    /**
     * Implements Map.put and related methods
     *
     * @param hash hash for key
     * @param key the key
     * @param value the value to put
     * @param onlyIfAbsent if true, don't change existing value
     * @param evict if false, the table is in creation mode.
     * @return previous value, or null if none
     */
    final V putVal(int hash, K key, V value, boolean onlyIfAbsent,
                   boolean evict) {
        // 创建一些变量
        Node<K,V>[] tab;
        Node<K,V> p;
        int n, i;

        // 把成员变量 table（即 容器）赋值给变量 tab，然后判断是否为 null
        // 如果变量 tab 为 null（其实就是成员变量 table（即 容器）为 null）
        // 或者
        // 变量的长度为 0（其实就是成员变量 table（即 容器）的长度为 0）
        // 那么
        // 这个就表示当前是第一次调用 put() 方法让容器中存放键值对（或者是调用了 remove() 方法删除了 HashMap 容器中的所有元素）
        if ((tab = table) == null || (n = tab.length) == 0)
            // 调用 resize() 方法创建一个容器（即 成员变量 table）并把容器的大小赋值给成员变量 n
            //（对于调用了 remove() 方法删除了 HashMap 容器中的所有元素的情况，resize() 方法将直接返回现有的容器）
            n = (tab = resize()).length;

        // 使用键（Key）的哈希值，和容器（即 成员变量 table 数组）的大小进行取模运算
        //（即 下面 if 语句中的 (n - 1) & hash）
        // 以获取当前元素（即 键值对）在容器中的存放位置（即 槽位 / 下标）
        //（在 HashMap 中，槽位也叫 “桶”（具体见 HashMap 笔记中的 “Java 7 中的存储结构”））
        //（具体见 HashMap 笔记中的 “哈希运算（取模）”）
        if ((p = tab[i = (n - 1) & hash]) == null)
            // 如果走到这个 if 语句当中
            // 说明当前槽位为空，（即 当前这个元素（键值对）在容器中没有产生哈希冲突）
            // 那么
            // 就直接往该槽位中存放键值对（即 Node 类实例）
            tab[i] = newNode(hash, key, value, null);
        else {
            // 如果走到这个 else 当中，那么就表示当前这个槽位上已经存放有元素了（即 键值对（即 Node 类实例））也就是产生了 “哈希冲突”
            Node<K,V> e;
            K k;

            // 这个 if 用于判断当前我们调用 put() 方法存入的键值对，和当前这个槽位上的键值对的键（Key）是否相等
            // 如果相等
            // 就表示我们当前往 HashMap 容器中存放键值对的操作是替换原有键值对的值（Value）的操作）
            if (p.hash == hash &&
                ((k = p.key) == key || (key != null && key.equals(k))))
                e = p;

            else if (p instanceof TreeNode)
                // 走到这个 else if 时，表示当前调用 put() 方法存入的键值对，和当前这个槽位上的键值对不是同一个
                // 并且
                // 当前这个槽位上挂着的是 “红黑树”（因为 p 的数据类型是 TreeNode 内部类）
                //（由于此时这个槽位上已经挂着多个元素（键值对）了，因此这个槽位也可以看做 “桶”）
                // 因此
                // 这里就调用 TreeNode 内部类中的 putTreeVal() 方法，采用红黑树的方式，往树中放入元素（即 键值对（Node 类实例））
                //（如果红黑树中存在和我们当前要存入的键值对相同的键值对，该 putTreeVal() 方法将返回该键值对）
                e = ((TreeNode<K,V>)p).putTreeVal(this, tab, hash, key, value);
            else {
                // 走到这个 else 时，表示当前调用 put() 方法存入的键值对，和当前这个槽位上的键值对不是同一个
                // 并且
                // 当前这个槽位上挂着的是 “链表”（因为 p 的数据类型 “不是” TreeNode 内部类）
                //（由于此时这个槽位上已经挂着多个元素（键值对）了，因此这个槽位也可以看做 “桶”）
                // 因此
                // 这里就直接往链表中存放元素（即 键值对（Node 类实例））
                for (int binCount = 0; ; ++binCount) {
                    if ((e = p.next) == null) {
                        p.next = newNode(hash, key, value, null);
                        // 树化操作
                        if (binCount >= TREEIFY_THRESHOLD - 1) // -1 for 1st
                            treeifyBin(tab, hash);
                        break;
                    }
                    // 上面的 if 语句只是对槽位上的第一个元素（当代码执行到这里的时候，那个第一个元素就是链表头）
                    // 判断该元素（键值对）是否和我们此时调用 put() 方法往容器中存放的键值对是否是同一个
                    // 这里的 if 判断的是链表中是否存在和我们此时要放入的键值对相同的键值对
                    if (e.hash == hash &&
                        ((k = e.key) == key || (key != null && key.equals(k))))
                        break;

                    // 把当前链表中的节点赋值给变量 p，以便获取下一个节点
                    p = e;
                }
            }

            // 当走到这个 if 判断里面去的时候（即 e 不为空）
            // 就表示
            // 当前 HashMap 容器中存在和我们此时需要存放的键值对相同的键值对
            //（即 要么是当前这个槽位上就是这个相同的键值对，要么就是就红黑树中有相同的键值对，要么就是链表中有相同的键值对）
            // 此时
            // 就会把我们此时调用 put() 方法传入的键（Value）替换掉老的键（Value）
            if (e != null) { // existing mapping for key
                V oldValue = e.value;
                if (!onlyIfAbsent || oldValue == null)
                    e.value = value;
                afterNodeAccess(e);
                return oldValue;
            }
        }

        ++modCount;

        // 扩容操作
        if (++size > threshold)
            resize();

        afterNodeInsertion(evict);

        return null;
    }

    /**
     * Initializes or doubles table size.  If null, allocates in
     * accord with initial capacity target held in field threshold.
     * Otherwise, because we are using power-of-two expansion, the
     * elements from each bin must either stay at same index, or move
     * with a power of two offset in the new table.
     *
     * 上面注释的含义为
     * 初始化（即 创建）容器（即 成员变量 table）或者把容器（即 成员变量 table）扩容一倍
     * 如果容器（即 成员变量 table）为 null（即 当前还没有容器）
     * 那么
     * 就使用成员变量 threshold（即 阈值）作为初始容器大小来创建一个容器
     * 其他情况下（其实就是指扩容的情况）
     * 把当前容器的大小直接扩容一倍，此时，容器中的元素要么保持原位，或者，会发生 2 的幂的偏移
     *（即 原槽位上的元素会转移到下一个 2 的幂距离的槽位上）（具体怎么样，待研究）
     *
     * 注意
     * 该方法只有在两种情况下会被调用
     * 即
     * 1. 第一次调用 put() 方法往 HashMap 容器中存放元素时，需要创建容器（即 成员变量 table）
     * 2. 容器（即 成员变量 table）的大小达到阈值，需要扩容
     *
     * @return the table
     */
    final Node<K,V>[] resize() {
        // 把成员变量 table （容器）赋值给变量 oldTab
        Node<K,V>[] oldTab = table;

        // 把容器（即 成员变量 table ）的大小赋值给变量 oldCap
        int oldCap = (oldTab == null) ? 0 : oldTab.length;

        // 把成员变量 threshold（即 阈值）赋值给变量 oldThr
        int oldThr = threshold;

        int newCap, newThr = 0;

        // 如果走了这个 if 语句，表示当前容器（即 成员变量 table）以及存在了（即 我们已经不是第一次调用 put() 方法往容器中存放键值对了）
        if (oldCap > 0) {
            if (oldCap >= MAXIMUM_CAPACITY) {
                threshold = Integer.MAX_VALUE;
                return oldTab;
            }
            // if 语句中的位运算表示把 oldCap 的值翻一倍（即 * 2）
            else if ((newCap = oldCap << 1) < MAXIMUM_CAPACITY &&
                     oldCap >= DEFAULT_INITIAL_CAPACITY)
                // 该位运算表示把 oldThr 的值翻一倍（即 * 2）
                newThr = oldThr << 1; // double threshold
        }
        // 如果走的是这个表示是第一次调用 put() 方法往 HashMap 容器中存放元素
        // 并且
        // 在创建 HashMap 类实例时，调用的是有参构造方法（因为成员变量 threshold 的值不为 0（具体可见那三个有参构造方法））
        else if (oldThr > 0) // initial capacity was placed in threshold
            newCap = oldThr;
        // 如果走的是这个表示是第一次调用 put() 方法往 HashMap 容器中存放元素
        // 并且
        // 在创建 HashMap 类实例时，调用的是无参构造方法（因为成员变量 threshold 的值为 0（具体可见那无参构造方法））
        else {               // zero initial threshold signifies using defaults
            newCap = DEFAULT_INITIAL_CAPACITY;
            newThr = (int)(DEFAULT_LOAD_FACTOR * DEFAULT_INITIAL_CAPACITY);
        }

        // 如果走的是上面的 else if (oldThr > 0)，那么就会走这个 if
        if (newThr == 0) {
            float ft = (float)newCap * loadFactor;
            newThr = (newCap < MAXIMUM_CAPACITY && ft < (float)MAXIMUM_CAPACITY ?
                      (int)ft : Integer.MAX_VALUE);
        }
        threshold = newThr;

        // 这一步就是创建容器（即 成员变量 table）（即 创建一个元素）
        @SuppressWarnings({"rawtypes","unchecked"})
            Node<K,V>[] newTab = (Node<K,V>[])new Node[newCap];

        table = newTab;

        // 如果 oldTab 不为空，即表示当前存在容器（即 成员变量 table 数组）
        // 即
        // 此时调用该方法的目的是为了给容器（即 成员变量 table 数组）扩容
        //（下面就是链表和红黑数的操作，就不具体写注释了，以后可以再补充）
        if (oldTab != null) {
            for (int j = 0; j < oldCap; ++j) {
                Node<K,V> e;
                if ((e = oldTab[j]) != null) {
                    oldTab[j] = null;
                    if (e.next == null)
                        newTab[e.hash & (newCap - 1)] = e;
                    else if (e instanceof TreeNode)
                        ((TreeNode<K,V>)e).split(this, newTab, j, oldCap);
                    else { // preserve order
                        Node<K,V> loHead = null, loTail = null;
                        Node<K,V> hiHead = null, hiTail = null;
                        Node<K,V> next;
                        do {
                            next = e.next;
                            if ((e.hash & oldCap) == 0) {
                                if (loTail == null)
                                    loHead = e;
                                else
                                    loTail.next = e;
                                loTail = e;
                            }
                            else {
                                if (hiTail == null)
                                    hiHead = e;
                                else
                                    hiTail.next = e;
                                hiTail = e;
                            }
                        } while ((e = next) != null);
                        if (loTail != null) {
                            loTail.next = null;
                            newTab[j] = loHead;
                        }
                        if (hiTail != null) {
                            hiTail.next = null;
                            newTab[j + oldCap] = hiHead;
                        }
                    }
                }
            }
        }
        return newTab;
    }

    /**
     * Replaces all linked nodes in bin at index for given hash unless
     * table is too small, in which case resizes instead.
     *
     * 该方法用于将容器（成员变量 table 数组）中槽位上挂着的链表转为红黑树
     */
    final void treeifyBin(Node<K,V>[] tab, int hash) {
        int n, index; Node<K,V> e;
        if (tab == null || (n = tab.length) < MIN_TREEIFY_CAPACITY)
            resize();
        else if ((e = tab[index = (n - 1) & hash]) != null) {
            TreeNode<K,V> hd = null, tl = null;
            do {
                TreeNode<K,V> p = replacementTreeNode(e, null);
                if (tl == null)
                    hd = p;
                else {
                    p.prev = tl;
                    tl.next = p;
                }
                tl = p;
            } while ((e = e.next) != null);
            if ((tab[index] = hd) != null)
                hd.treeify(tab);
        }
    }

